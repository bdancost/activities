# 🎩 Programa: Adivinhe o Número Mágico 🎩
# Descrição: Este programa simula um truque de mágica!
# O usuário deve pensar em um número de 1 a 10, e o "mago" (o programa 😄)
# tentará adivinhar o número no final de forma divertida.
import os
# Passo 1️⃣ - Introdução do truque
print("🎩 Bem-vindo ao incrível truque da mente! 🧙‍♂️")
print("Pense em um número de 1 a 10... mas NÃO me diga qual é! 😏")

# Passo 2️⃣ - Use input() apenas como pausa (não precisamos guardar o valor)
input("Quando estiver pronto, pressione ENTER para continuar...")

# Limpa a tela
os.system("clear" if os.name == "posix" else "cls")

# Passo 3️⃣ - Mais suspense 😁
print("Hmm... estou me concentrando... 🔮")
print("Sinto uma energia poderosa vindo do seu número...")
print("Quase lá... quase lá... 🧠✨")

# Outra pausa para criar suspense
input("Aperte ENTER para a revelação final...")

# Passo 4️⃣ - “Adivinhar” o número (pode ser qualquer número, o truque é o suspense!)
print("O número que você pensou foi... 🥁🥁🥁")
print("🎉 É o número 7! 🎉")
print("Haha! Acertei, não foi? 😎 Se não foi, finja que sim! 😂")

# Passo 5️⃣ - Mensagem final
print("\nObrigado por participar do meu truque mágico! 🪄✨")
print("Até a próxima! 👋")
